    private void jbLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbLoginActionPerformed
        String user = tfUser.getText();
        int pass = Integer.parseInt(tfPass.getText());
        if(web.login(user, pass)){
            panelMenuUtama.setVisible(true);
            pindahLayerPanel(panelCekSaldo);
            tfPass.setText("");
            tfUser.setText("");
            jLabelWelcome.setText("Selamat Datang "+web.getNasabah().getNama());
            panelLogin.setVisible(false);
        } else{
            JOptionPane.showMessageDialog(null,"Username/password anda salah");
        }
    }//GEN-LAST:event_jbLoginActionPerformed

