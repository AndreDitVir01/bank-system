    private void jbTransferUangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbTransferUangActionPerformed
        int rekening = Integer.parseInt(tfRekeningTujuan.getText());
        long nominal = Long.parseLong(tfNominalTransfer.getText());
        if(web.transfer(nominal, rekening, false)){
            jLabelRekeningTransfer.setText(""+rekening);
            jLabelNominalTransfer.setText(""+nominal);
            jLabelNamaUserTransfer.setText(""+web.getNasabah().getNama());
            pindahLayerPanel(panelCekUserTransfer);
        }
        else{
            JOptionPane.showMessageDialog(null,"Rekening tidak Ditemukan/Saldo tidak cukup!!");
        }
    }//GEN-LAST:event_jbTransferUangActionPerformed

