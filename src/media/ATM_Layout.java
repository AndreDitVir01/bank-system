    private void jbTarikTunaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbTarikTunaiActionPerformed
        long nominal = Long.parseLong(tfNominalAmbil.getText());

        if((nominal % 100000) == 0){
            tarikTunaiKonstan(nominal);
            labelSaldoAndaTransaksi.setText(""+atm.cekSaldo()+",00");
            panelCekSaldoTransaksi.setVisible(true);
            panelTarikTunai.setVisible(false);
        }else{
            tfNominalAmbil.setText("");
            jLabelPeringatanTarikTunai.setText("Maaf, penarikan hanya untuk pecahan Rp. 100.000, -");
        }
    }//GEN-LAST:event_jbTarikTunaiActionPerformed

