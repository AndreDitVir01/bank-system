    private void jbSimpanUangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSimpanUangActionPerformed
        long nominal = Long.parseLong(tfNominalSimpan.getText());
        long sisa = 0; 
        Tipe_Kartu tipe = atm.getTabungan().getKartuATM().getTipeKartu();
                    int jawab = JOptionPane.showConfirmDialog(this, "Silahkan konfirmasi?");
                        switch(jawab){
                            case JOptionPane.YES_OPTION:
                                sisa = (nominal % 100000);
                                nominal = nominal - sisa;
                                if(atm.setorTunai(nominal,tipe)){
                                    JOptionPane.showMessageDialog(null,"Total uang senilai "+nominal+" berhasil disetorkan");
                                }
                                break;
                            case JOptionPane.NO_OPTION:
                                JOptionPane.showMessageDialog(this,"Anda telah membatalkan setor tunai");
                                break;
                            case JOptionPane.CANCEL_OPTION:
                                JOptionPane.showMessageDialog(this,"Anda telah membatalkan setor tunai");
                                break;
                        }
                JOptionPane.showMessageDialog(null,"Sisa : "+sisa+"\nSilahkan, tunggu uang Anda");
                labelSaldoAndaTransaksi.setText(""+atm.cekSaldo()+",00");
                panelCekSaldoTransaksi.setVisible(true);
                panelSetorTunai.setVisible(false);
    }//GEN-LAST:event_jbSimpanUangActionPerformed

