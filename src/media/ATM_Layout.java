    private void jbTransferUangOkeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbTransferUangOkeActionPerformed
        if(atm.transfer(Integer.parseInt(jLabelNominalTransfer.getText()), Integer.parseInt(jLabelRekeningTransfer.getText()), true)){
            labelSaldoAndaTransaksi.setText(""+atm.cekSaldo()+",00");
            jLabelRekeningTransfer.setText(null);
            jLabelNamaUserTransfer.setText(null);
            jLabelNominalTransfer.setText(null);
            tfRekeningTujuan.setText(null);
            panelCekSaldoTransaksi.setVisible(true);
            panelCekUserTransfer.setVisible(false);
        }
    }//GEN-LAST:event_jbTransferUangOkeActionPerformed

